SOCIAL_VISUAL_TRENDS = """
你需要结合 2024-2026 在小红书常见的高点击视觉风格：
1) 高饱和渐变背景 + 白色粗体大字
2) 便签贴纸、箭头、emoji 引导重点
3) 对比型封面文案（例如“刷短剧 vs 赚钱刷短剧”）
4) 大数字利益点（如“每天30-80元”）
5) 强场景化（宝妈碎片时间/大学生宿舍时间）
""".strip()


def build_softpost_prompt(topic: str, audience: str, monetization_core: str) -> str:
    return f"""
你是小红书增长运营+文案策划+视觉策划专家。
请围绕选题“{topic}”，受众“{audience}”，核心卖点“{monetization_core}”产出内容。

{SOCIAL_VISUAL_TRENDS}

输出严格 JSON（不要 markdown 代码块，不要解释），字段：
- title: 18-26字，抓眼球但不过度夸张
- body: 280-450字，软文口吻，分段，每段不超过70字，包含真实场景感、步骤感、轻CTA
- hashtags: 数组，8-12个中文标签，含受众标签+变现标签+场景标签
- image_prompt: 用于图片生成的中文提示词，描述“高点击封面图”，包括配色、构图、文案位置、贴纸元素、人物状态

要求：
1) 标题必须体现“低门槛+碎片时间+可见收益”三者至少两项。
2) 正文包含“风险提示”一句，避免绝对收益承诺。
3) 语气真实，像普通用户分享经验，不要像广告法违规文案。
""".strip()
