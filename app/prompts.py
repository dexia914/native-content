SOCIAL_VISUAL_TRENDS = """
你需要结合 2024-2026 在小红书常见的高点击视觉风格：
1) 高饱和渐变背景和醒目主体
2) 卡片、贴纸、箭头、标签等视觉元素
3) 强场景化，比如宝妈带娃空档、大学生宿舍碎片时间
4) 强对比构图，但画面不要过满
5) 封面底图必须适合后期排版
""".strip()


def build_softpost_prompt(topic: str, audience: str, monetization_core: str) -> str:
    return f"""
你是小红书增长运营、文案策划和视觉策划专家。
请围绕选题“{topic}”、受众“{audience}”、核心卖点“{monetization_core}”产出内容。

{SOCIAL_VISUAL_TRENDS}

输出严格 JSON，不要 markdown 代码块，不要解释。字段：
- title: 18-26字，抓眼球但不过度夸张
- body: 280-450字，软文口吻，分段，包含真实场景、步骤感和轻 CTA
- hashtags: 数组，8-12个中文标签，覆盖受众标签、变现标签、场景标签
- image_prompt: 用于图片生成的中文提示词，要求生成“适合后期排版的封面底图”

图片要求：
1) 不要在图片里生成任何文字、标题、数字、水印、logo、按钮、界面截图
2) 不要出现中文、英文、繁体字、字母、伪文字
3) 只生成人物、场景、配色、构图、贴纸感元素和留白区域
4) 画面上方和下方保留明显留白，方便后续程序叠加中文标题和标签
5) 风格要像小红书爆款封面底图，但底图本身不带字

文案要求：
1) 标题必须体现“低门槛 + 碎片时间 + 可见收益”三者中至少两个
2) 正文包含一句风险提示，避免绝对收益承诺
3) 语气真实，像普通用户分享经验，不要像硬广
""".strip()
